    /**
     * Updates an existing Call model.
     * If update is successful, the browser will be redirected to the 'view' page.
     * @param integer $id
     * @return mixed
     */
    public function actionUpdate($id)
    {
        $model = $this->findModel($id);

        if ($model->load(Yii::$app->request->post()) && $model->save()) {
            Yii::$app->session->setFlash('success', 'Your comment was successfully updated');
            return $this->redirect(['view', 'id' => $model->id]);
        } else {
            return $this->render('update', [
                'model' => $model,
            ]);
        }
    }

    /**
     * Deletes an existing Call model.
     * If deletion is successful, the browser will be redirected to the 'index' page.
     * @param integer $id
     * @return mixed
     */
    public function actionDelete($id)
    {
        $this->findModel($id)->delete();

        return $this->redirect(['index']);
    }

    /**
     * Finds the Call model based on its primary key value.
     * If the model is not found, a 404 HTTP exception will be thrown.
     * @param integer $id
     * @return Call the loaded model
     * @throws NotFoundHttpException if the model cannot be found
     */
    protected function findModel($id)
    {
        if (($model = Call::findOne($id)) !== null) {
            return $model;
        } else {
            throw new NotFoundHttpException('The requested page does not exist.');
        }
    }

    public function actionStat()
    {
        $quantity = Call::getQuan();
        $incoming = Call::getIncoming();
        $outgoing = Call::getOutgoing();
        $avg_duration = Call::getAvgDuration();
        $max_duration = Call::getMaxDuration();
        $max_used_num = Call::getMaxUsedNumber();

        return $this->render('www', [
            'quantity' => $quantity,
            'incoming' => $incoming,
            'outgoing' => $outgoing,
            'avg_duration' => $avg_duration,
            'max_duration' => $max_duration,
            'max_used_num' => $max_used_num
        ]);
    }
}
